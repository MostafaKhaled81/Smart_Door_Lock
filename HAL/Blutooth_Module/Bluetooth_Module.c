/******************************************************************************
* \Syntax          : void BLUTOOTH_MODULE_INIT(void)       
* \Description     : Describe this service                                    
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : parameterName   Parameter Describtion                     
* \Parameters (out): None                                                      
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK                                  
*******************************************************************************/
void BLUTOOTH_MODULE_INIT(void)
{
	IRQH_SetCallBack(USART_Rx_Complete_VECTOR_INDEX,UART_ReceiveChar);
	UART_Init();
}

/******************************************************************************
* \Syntax          : uint8 BLUTOOTH_RecieveChar(void)
* \Description     : Describe this service
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \ (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK
*******************************************************************************/
uint8 BLUETOOTH_RecieveChar(void)
{
	while(UART_RecieveStatus()==0);
	return U_REC_DATA;
}
